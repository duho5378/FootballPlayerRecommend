from mongoengine import Document, EmbeddedDocument, fields

class PlayerSocial(EmbeddedDocument):
    follow = fields.IntField(required=True)
    like = fields.IntField(required=True)
    dislike = fields.IntField(required=True)

class PlayerSkills(EmbeddedDocument):
    crossing = fields.IntField(required=True, null=False)
    finishing = fields.IntField(required=True, null=False)
    heading_accuracy = fields.IntField(required=True, null=False)
    short_passing = fields.IntField(required=True, null=False)
    volleys = fields.IntField(required=True, null=False)
    dribbling = fields.IntField(required=True, null=False)
    curve = fields.IntField(required=True, null=False)
    fk_accuracy = fields.IntField(required=True, null=False)
    long_passing = fields.IntField(required=True, null=False)
    ball_control = fields.IntField(required=True, null=False)
    acceleration = fields.IntField(required=True, null=False)
    sprint_speed = fields.IntField(required=True, null=False)
    agility = fields.IntField(required=True, null=False)
    reactions = fields.IntField(required=True, null=False)
    balance = fields.IntField(required=True, null=False)
    shot_power = fields.IntField(required=True, null=False)
    jumping = fields.IntField(required=True, null=False)
    stamina = fields.IntField(required=True, null=False)
    strength = fields.IntField(required=True, null=False)
    long_shots = fields.IntField(required=True, null=False)
    aggression = fields.IntField(required=True, null=False)
    interceptions = fields.IntField(required=True, null=False)
    positioning = fields.IntField(required=True, null=False)
    vision = fields.IntField(required=True, null=False)
    penalties = fields.IntField(required=True, null=False)
    composure = fields.IntField(required=True, null=False)
    marking = fields.IntField(required=True, null=False)
    standing_tackle = fields.IntField(required=True, null=False)
    sliding_tackle = fields.IntField(required=True, null=False)
    gk_diving = fields.IntField(required=True, null=False)    
    gk_handling = fields.IntField(required=True, null=False)
    gk_kicking = fields.IntField(required=True, null=False)
    gk_positioning = fields.IntField(required=True, null=False)
    gk_reflexes = fields.IntField(required=True, null=False)

class Player(Document):
    name = fields.StringField(required=True, null=False)
    photo_url = fields.StringField(required=True, null=True)
    positions = fields.ListField(fields.StringField(required=True, null=True))
    age = fields.StringField(required=True)
    birth_date = fields.StringField(required=True)
    height = fields.IntField(required=True)
    weight = fields.IntField(required=True)
    overall_rating = fields.IntField(required=True)
    potential = fields.IntField(required=True)
    value = fields.StringField(required=True)
    wage = fields.StringField(required=True)
    preferred_foot = fields.StringField(required=True)
    international_reputation = fields.IntField(required=True)
    weak_foot = fields.IntField(required=True)
    skill_moves = fields.IntField(required=True)
    work_rate = fields.StringField(required=True)
    body_type = fields.StringField(required=True)
    real_face = fields.StringField(required=True)
    release_clause = fields.StringField(default='0')
    player_hashtags = fields.ListField(fields.StringField(required=True, null=True))
    social = fields.EmbeddedDocumentField(PlayerSocial)
    skills = fields.EmbeddedDocumentField(PlayerSkills)
    player_traits = fields.ListField(fields.StringField(null=True), null=True)

